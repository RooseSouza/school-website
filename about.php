        <br><br>
        <strong>Expansion & Registration (2011-2012):</strong><br>
        In 2011, the management decided to formally register the Saunstha. It was in the same year that we made the
        important decision to start the Primary section in Marathi medium. We applied to the Department of Education for
        permission to commence the primary section, and after receiving approval, STD I class was started in the
        academic year 2013-14.
        <br><br>
        <strong>Continuous Growth:</strong><br>
        There was no looking back after that. We have been adding subsequent classes every year (STD II, STD III, STD
        IV) and today have a total of around 100 students in the primary section. It is only because of the dedicated
        efforts of our teaching and non-teaching staff, combined with equal support from the management, that we have
        been able to operate classes with increasing strength over these years.
        <br><br>
        <strong>Middle School Expansion (2020-21):</strong><br>
        To further expand our educational offerings and provide comprehensive schooling, we launched the Middle School
        section from the academic year 2020-21. This expansion demonstrates our commitment to serving the educational
        needs of the community and providing a complete learning journey for our students.
        <br><br>
        <strong>Proof of Excellence:</strong><br>
        We take great pride in informing you that the students who have joined other schools after completing their
        course in our primary section have continued to shine academically in their new institutions as well. This is a
        testament to the quality of education we provide and the strong foundation we build for every child.
